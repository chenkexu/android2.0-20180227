public class MeBean {

    private String name;
    private String userHead;
    private int commission;
    private String phone;
    private int balance;
    private int frozenAmount;
    private String moreMoney;
    private String subCount;
    private int moneyStatus;
    private String upMoney;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUserHead() {
        return userHead;
    }

    public void setUserHead(String userHead) {
        this.userHead = userHead;
    }

    public int getCommission() {
        return commission;
    }

    public void setCommission(int commission) {
        this.commission = commission;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getBalance() {
        return balance;
    }

    public void setBalance(int balance) {
        this.balance = balance;
    }

    public int getFrozenAmount() {
        return frozenAmount;
    }

    public void setFrozenAmount(int frozenAmount) {
        this.frozenAmount = frozenAmount;
    }

    public String getMoreMoney() {
        return moreMoney;
    }

    public void setMoreMoney(String moreMoney) {
        this.moreMoney = moreMoney;
    }

    public String getSubCount() {
        return subCount;
    }

    public void setSubCount(String subCount) {
        this.subCount = subCount;
    }

    public int getMoneyStatus() {
        return moneyStatus;
    }

    public void setMoneyStatus(int moneyStatus) {
        this.moneyStatus = moneyStatus;
    }

    public String getUpMoney() {
        return upMoney;
    }

    public void setUpMoney(String upMoney) {
        this.upMoney = upMoney;
    }
}
